package com.water.table;

public class TbCabinet {

	private String ip;
	private String bigZone;
	private String smallZone;
	private String startWristNo;
	private int cabNo;
	private int type;
	private int boxNum;
	
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getBigZone() {
		return bigZone;
	}
	public void setBigZone(String bigZone) {
		this.bigZone = bigZone;
	}
	public String getSmallZone() {
		return smallZone;
	}
	public void setSmallZone(String smallZone) {
		this.smallZone = smallZone;
	}
	public int getCabNo() {
		return cabNo;
	}
	public void setCabNo(int cabNo) {
		this.cabNo = cabNo;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getBoxNum() {
		return boxNum;
	}
	public void setBoxNum(int boxNum) {
		this.boxNum = boxNum;
	}
	public String getStartWristNo() {
		return startWristNo;
	}
	public void setStartWristNo(String startWristNo) {
		this.startWristNo = startWristNo;
	}
	
}

package com.water.table;

public class TbRecord {

	private int cabNo;
	private int boxNo;
	private String wristNo;
	private String optTime;
	private String cardNo;
	
	public int getCabNo() {
		return cabNo;
	}
	public void setCabNo(int cabNo) {
		this.cabNo = cabNo;
	}
	public int getBoxNo() {
		return boxNo;
	}
	public void setBoxNo(int boxNo) {
		this.boxNo = boxNo;
	}
	public String getWristNo() {
		return wristNo;
	}
	public void setWristNo(String wristNo) {
		this.wristNo = wristNo;
	}
	public String getOptTime() {
		return optTime;
	}
	public void setOptTime(String optTime) {
		this.optTime = optTime;
	}
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	
}
